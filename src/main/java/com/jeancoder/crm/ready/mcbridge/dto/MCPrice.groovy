package com.jeancoder.crm.ready.mcbridge.dto

import com.jeancoder.crm.ready.mcbridge.ret.MCRet

class MCPrice extends MCRet {
	private String price;
	
	private String handle_fee;
	
	private String per_handle_fee;
	
	private String handle_fee_by;
	
	private String total_allowance;
	
	private String mckey;
	
	private String mctype;
	
	private Long marketId;
	private String marketItemId;
	
	public MCPrice() {
		super();
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getMckey() {
		return mckey;
	}

	public void setMckey(String mckey) {
		this.mckey = mckey;
	}

	public String getMctype() {
		return mctype;
	}

	public void setMctype(String mctype) {
		this.mctype = mctype;
	}

	public String getHandle_fee() {
		return handle_fee;
	}

	public void setHandle_fee(String handle_fee) {
		this.handle_fee = handle_fee;
	}

	public String getHandle_fee_by() {
		return handle_fee_by;
	}

	public void setHandle_fee_by(String handle_fee_by) {
		this.handle_fee_by = handle_fee_by;
	}

	public String getPer_handle_fee() {
		return per_handle_fee;
	}

	public void setPer_handle_fee(String per_handle_fee) {
		this.per_handle_fee = per_handle_fee;
	}

	public Long getMarketId() {
		return marketId;
	}

	public void setMarketId(Long marketId) {
		this.marketId = marketId;
	}

	public String getMarketItemId() {
		return marketItemId;
	}

	public void setMarketItemId(String marketItemId) {
		this.marketItemId = marketItemId;
	}

	public String getTotal_allowance() {
		return total_allowance;
	}

	public void setTotal_allowance(String total_allowance) {
		this.total_allowance = total_allowance;
	}
	
}
